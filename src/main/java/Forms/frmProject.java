    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        this.dispose();
        try {
            readyFunctions.openForm(new frmMain());
        } catch (IOException ex) {
            readyFunctions.msgBox(ex.getMessage());
        }
    }//GEN-LAST:event_btnBackActionPerformed
    public void clearData(){
        readyFunctions.clearText(this);
        txtProjectNO.setText(proj.getAutoNumber());
        btnAdd.setEnabled(true);
        btnDelete.setEnabled(false);
        btnUpdate.setEnabled(false);
        txtProjectName.requestFocus();
    }
    
    public void setValues(){
        proj.setProjectno(Integer.parseInt(txtProjectNO.getText()));
        proj.setProjctname(txtProjectName.getText());
        proj.setLocation(txtLocation.getText());
        //proj.setDeptno(Integer.parseInt(dept.getValueByName(cbxDept.getSelectedItem().toString())));
        
        String deptName = cbxDept.getSelectedItem().toString();
        int deptNo = Integer.parseInt(dept.getValueByName(deptName));
        proj.setDeptno(deptNo);
    }
