    private void tblProjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProjectMouseClicked
        int row = tblProject.getSelectedRow();
        
        txtProjectNO.setText((String)tblProject.getValueAt(row, 0));
        txtProjectName.setText((String)tblProject.getValueAt(row, 1));
        txtLocation.setText((String)tblProject.getValueAt(row, 2));
        cbxDept.setSelectedItem(tblProject.getValueAt(row, 4));
        
        btnAdd.setEnabled(false);
        btnUpdate.setEnabled(true);
        btnDelete.setEnabled(true);
        
    }//GEN-LAST:event_tblProjectMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String strSearch = "select * from project_data where ";
        String mylike = "like '%" + txtSearch.getText() + "%'";
        
        if(rdoProjectNO.isSelected()) strSearch += "number" + mylike;
        else if(rdoName.isSelected()) strSearch += "name" + mylike;
        else if(rdoLocation.isSelected()) strSearch += "location" + mylike;
        else strSearch += "department" + mylike;
        
        proj.getCoustmRow(strSearch, tblProject);
    }//GEN-LAST:event_btnSearchActionPerformed

