    private void tblDeptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDeptMouseClicked
        try{
            int selectedRow = tblDept.getSelectedRow();
            
            String No = (String)tblDept.getValueAt(selectedRow, 0);
            String Nam = (String)tblDept.getValueAt(selectedRow, 1);
            String Loc = (String)tblDept.getValueAt(selectedRow, 2);
            
            txtDeptNum.setText(No);
            txtDeptName.setText(Nam);
            txtDeptLocation.setText(Loc);
            
            btnAdd.setEnabled(false);
            btnDelete.setEnabled(true);
            btnUpdate.setEnabled(true);
            
        }
        catch(Exception ex){
            Company.readyFunctions.msgBox(ex.getMessage());
        }
    }//GEN-LAST:event_tblDeptMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String search = "select * from department where ";
        if(rdoNumber.isSelected()) search += "deptno";
        else if(rdoName.isSelected()) search += "deptname";
        else search += "location";
        
        search += " like '%" + txtSearch.getText() + "%'";
        dept.getCoustmRow(search, tblDept);
        
    }//GEN-LAST:event_btnSearchActionPerformed

