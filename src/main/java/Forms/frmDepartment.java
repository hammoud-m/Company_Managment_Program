    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //this to kopla rdobtns together
        btnGroup.add(rdoNumber);
        btnGroup.add(rdoName);
        btnGroup.add(rdoLocation);
        clearData();
        //this to update the table 
        dept.getAllRows(tblDept);
        //this to places of rows cant changed
        tblDept.getTableHeader().setReorderingAllowed(false);
        
    }//GEN-LAST:event_formWindowOpened

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        this.dispose();
        try {
            Company.readyFunctions.openForm(new frmMain());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }//GEN-LAST:event_btnBackActionPerformed
    Entity.Department dept = new Entity.Department();
    public void setValues()
    {
        dept.setDeptno(Integer.parseInt(txtDeptNum.getText()));
        dept.setDeptname(txtDeptName.getText());
        dept.setLocation(txtDeptLocation.getText());
    }
    public void clearData()
    {
        Company.readyFunctions.clearText(this);
        txtDeptNum.setText(dept.getAutoNumber());
        btnAdd.setEnabled(true);
        btnUpdate.setEnabled(false);
        btnDelete.setEnabled(false);
        txtDeptName.requestFocus();
    }
    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        clearData();
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        setValues();
        dept.add();
        clearData();
        //this to update the table 
        dept.getAllRows(tblDept);
    }//GEN-LAST:event_btnAddActionPerformed

